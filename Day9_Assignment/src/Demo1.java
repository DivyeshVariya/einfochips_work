
public class Demo1 {

	public Demo1(String name, int runs) {
		super();
		this.name = name;
		this.runs = runs;
	}
	private String name;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRuns() {
		return runs;
	}
	public void setRuns(int runs) {
		this.runs = runs;
	}
	private int runs;
}
